package lethalhabit.math;

/**
 * Two-dimensional object with an x and y coordinate (e.g. {@link Point}, {@link Vec2D})
 */
public interface TwoDimensional {
    
    /**
     * X-coordinate of the two-dimensional
     *
     * @return The x-coordinate of the two-dimensional
     */
    double x();
    
    /**
     * Y-coordinate of the two-dimensional
     *
     * @return The y-coordinate of the two-dimensional
     */
    double y();
    
}
